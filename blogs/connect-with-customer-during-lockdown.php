                                    <p>Because of COVID – 19, the whole world is facing some sort of financial crisis. We are no longer different from others. People are losing their jobs now and then. Office, the industry can’t run properly. In this regard, we have to come up with some new ideas. And we plan to do our office work from home.
                                    </p>
                                    <p>To implement this idea, we need to discuss the platforms we can use, the benefits we will get from doing our respective jobs from home, and the problems we may face. <br>
                                    </p>
                                    <h6>Benefits of working from home:<br><br> </h6>
                                    <p>As human contact is highly prohibited during this period, so if we work from home, the chance of affection by the coronavirus will tend to zero. ‘No Journey’ means no wastage of time due to traffic jams and thus no tiredness at all. Moreover, we will get extra time to complete our tasks as no formal office hour is included here. So it will reduce our extra pressure as well. We can develop VR, AR to create an office atmosphere in our house. In this case, we won’t be bored with our continuous work. Whenever we need it, we can take a rest, have some homemade food which is safer than outside restaurants’ food and again we can be involved in our duty. Thus the progression will be faster. Group meetings can be done using video conferences. The project, case shows, can be done by using screen sharing and so on. In such a way, we will be able to do our jobs from home without wasting time, money, and extra labor.<br></p>
                                    <h6>Platforms which we can use:<br><br> </h6>
                                    <p>Facebook, Messenger, WhatsApp are the standard social media that we mostly use either for entertaining ourselves or to be connected with our f&f, colleagues. But we can use them differently. There are such applications that will connect all our company members in a common platform. Audio call, video call, video conference, screen sharing, task sharing, discussion all can be completed while using that software. Among them, ‘Google Meet,’ ‘Google Classroom,’ ‘Google G Suit’, ‘Zoom’ , ‘Slack’ , ‘Cisco’, ‘Discord’ are the most common and widely used software. Whatever, we often find them challenging to use. But why? The answer is simple; just because we don’t use them frequently. If we start using them on a regular basis, we will be used to handling their functionalities. Even by developing VR (Virtual Reality), we may create an atmosphere of virtual office in our home.<br></p>
                                    <h6>Problems that we may face:<br><br> </h6>
                                    <p>To ensure our working from our residence, we need a phone/tab, laptop/desktop, and an uninterrupted internet connection. Nowadays, smartphones, tabs, laptops are most common to people, but as in our country, it is challenging to have a 24/7 electricity facility so we might face some trouble while doing our work. And again, hardware basis works can’t be done in our house. Only the software part can be taken off. So there are some limitations as well. Another problem is we won’t be able to collaborate with our colleagues whenever we want. A specific time will be set for a video conference or group meeting. So connecting all the members within a specified period is questionable.<br></p>
                                    <p>But these problems can be mitigated. With comparison to the benefits of working from home, the issues that may occur are few. If we can make our room as a mini office, then why be taking extra pressure? So rethinking is a must in this case.
